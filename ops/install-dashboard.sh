while [[ $# -gt 0 ]]; do
  case "$1" in
    --name)
      INSTANCE="${2:?--name requires a value}"
      shift 2
      ;;
    --bind)
      BIND="${2:?--bind requires a value}"
      shift 2
      ;;
    --port)
      PORT="${2:?--port requires a value}"
      shift 2
      ;;
    --runtime-dir)
      RUNTIME_DIR="${2:?--runtime-dir requires a value}"
      RUNTIME_SET=1
      shift 2
      ;;
    --env-file)
      ENV_FILE="${2:?--env-file requires a value}"
      shift 2
      ;;
    --no-watchdog)
      INSTALL_WATCHDOG=0
      INSTALL_SYNC_COORDINATOR=0
      INSTALL_GUARDS=0
      shift
      ;;
    --no-sync-coordinator)
      INSTALL_SYNC_COORDINATOR=0
      shift
      ;;
    --no-guards)
      INSTALL_GUARDS=0
      shift
      ;;
    --no-start)
      START_SERVICES=0
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown option: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

